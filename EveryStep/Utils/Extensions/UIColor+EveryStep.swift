import UIKit

/**
    EveryStep color palette 
*/
extension UIColor {
    class func everyStepBlueColor() -> UIColor {
        return UIColor(red: 113.0/255.0, green: 197.0/255.0, blue: 255.0/255.0, alpha: 1.0)
    }
}
